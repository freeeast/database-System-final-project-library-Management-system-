-- create table - PUBLISHER 
CREATE TABLE PUBLISHER
(PUBLISHER_ID NUMBER(20) NOT NULL,
NAME VARCHAR(30),
PUBLICATION_YEAR DATE,
PRIMARY KEY(PUBLISHER_ID));

-- create tabel - BOOK
CREATE TABLE BOOK
(ISBN NUMBER(15) NOT NULL,
CATEGORY VARCHAR(20),
AUTHOR VARCHAR(15),
NAME VARCHAR(15),
PRIMARY KEY(ISBN));

-- create table - BOOKCOPY
CREATE TABLE BOOKCOPY
(ISBN NUMBER(15) REFERENCES BOOK,
CALL_NUMBER VARCHAR(30) NOT NULL,
AVAILABLE INTEGER,
B_RESERVE VARCHAR(30),
PRIMARY KEY(CALL_NUMBER));

-- create table - READER
CREATE TABLE READER
(SSN VARCHAR(25) NOT NULL,
NAME VARCHAR(15),
R_RESERVE_NO VARCHAR(30),
PRIMARY KEY(SSN));

-- create table - READER_PHONE
CREATE TABLE READER_PHONE
(SSN VARCHAR(25) REFERENCES READER,
PHONE_NO NUMBER(20));

-- create table - READER_EMAIL
CREATE TABLE READER_EMAIL
(SSN VARCHAR(25) REFERENCES READER,
EMAIL VARCHAR(30));

-- create table - STAFF
 CREATE TABLE STAFF
(STAFF_ID NUMBER(10) NOT NULL,
NAME VARCHAR(15),
EMAIL VARCHAR(30),
AREA VARCHAR(30),
PRIMARY KEY(STAFF_ID));


-- create table - REPORT
CREATE TABLE REPORT
(CALL_NUMBER VARCHAR(30) REFERENCES BOOKCOPY,
SSN VARCHAR(25) REFERENCES READER,
TIME DATE,
PRIMARY KEY(CALL_NUMBER,SSN,TIME));

-- create table - ACCOUNT
CREATE TABLE ACCOUNT
(SSN VARCHAR(25) REFERENCES READER,
LOGIN_ID VARCHAR(15) NOT NULL,
PASSAWARD VARCHAR(20),
CREDIT NUMBER(3),
BORROW_NO NUMBER(15),
PRIMARY KEY(LOGIN_ID));


-- create table - BORROW
CREATE TABLE BORROW
(BORROW_TIME DATE,
SSN VARCHAR(25) REFERENCES READER,
CALL_NUMBER VARCHAR(30) REFERENCES BOOKCOPY
PERFORM_STAFF VARCHAR(25) REFERENCES STAFF);

-- create table - RETURN
CREATE TABLE RETURN
(RETURN_TIME DATE,
SSN VARCHAR(25) REFERENCES READER,
CALL_NUMBER VARCHAR(30) REFERENCES BOOKCOPY);

-- create table - PUBLISH
CREATE TABLE PUBLISH
 (PUBLISHER_ID NUMBER(20) REFERENCES PUBLISHER,
 ISBN NUMBER(15) REFERENCES BOOK);

-- create table - MANAGE
CREATE TABLE MANAGE
 (STAFF_ID NUMBER(10) REFERENCES STAFF,
 CALL_NUMBER VARCHAR(30) REFERENCES BOOKCOPY);

-- create table - HAS
CREATE TABLE HAS
 (ISBN NUMBER(15) REFERENCES BOOK,
 CALL_NUMBER VARCHAR(30) REFERENCES BOOKCOPY);

-- create table - RECORD
CREATE TABLE RECORD
 (LOGIN_ID VARCHAR(15) REFERENCES ACCOUNT,
TIME DATE);

